export const decodedValue = (colors) => {
  return colors.slice(0, 2).reduce((pre, cur) => {
    return Number(pre + COLORS.indexOf(cur).toString());
  }, '');
};

export const COLORS = [
  'black',
  'brown',
  'red',
  'orange',
  'yellow',
  'green',
  'blue',
  'violet',
  'grey',
  'white',
];
